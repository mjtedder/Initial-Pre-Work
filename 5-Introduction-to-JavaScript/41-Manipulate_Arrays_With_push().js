/*

The .push method is an array method which adds data to the end of an array.  We can add one piece of data or
multiple pieces of data, we just add a comma to separate data.

*/

// Example
var ourArray = ["Stimpson", "J", "cat"];
ourArray.push(["happy", "joy"]); 
// ourArray now equals ["Stimpson", "J", "cat", ["happy", "joy"]]

